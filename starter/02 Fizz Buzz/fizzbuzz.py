# Print numbers from 1 to 100, replacing multiples of 3 with “Fizz” and multiples of 5 with “Buzz.”

for num in range(1, 101):
    if num % 3 == 0 and num % 5 == 0:
        print("FizzBuzz")
    elif num % 3 == 0:
        print("Fizz")
    elif num % 5 == 0:
        print("Buzz")
    else:
        print(num)